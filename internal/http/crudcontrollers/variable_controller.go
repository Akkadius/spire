type VariableController struct {
	db       *database.Resolver
	auditLog *auditlog.UserEvent
}

func NewVariableController(
	db *database.Resolver,
	auditLog *auditlog.UserEvent,
) *VariableController {
	return &VariableController{
		db:       db,
		auditLog: auditLog,
	}
}

func (e *VariableController) Routes() []*routes.Route {
	return []*routes.Route{
		routes.RegisterRoute(http.MethodGet, "variable/:id", e.getVariable, nil),
		routes.RegisterRoute(http.MethodGet, "variables", e.listVariables, nil),
		routes.RegisterRoute(http.MethodGet, "variables/count", e.getVariablesCount, nil),
		routes.RegisterRoute(http.MethodPut, "variable", e.createVariable, nil),
		routes.RegisterRoute(http.MethodDelete, "variable/:id", e.deleteVariable, nil),
		routes.RegisterRoute(http.MethodPatch, "variable/:id", e.updateVariable, nil),
		routes.RegisterRoute(http.MethodPost, "variables/bulk", e.getVariablesBulk, nil),
	}
}

